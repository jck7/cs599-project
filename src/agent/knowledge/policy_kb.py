class PolicyKnowledgeBase:
    """财务制度知识库"""

    def __init__(self):
        self._store = None

    @property
    def store(self):
        if self._store is None:
            self._store = get_vector_store("policy_kb")
            # 初始化时加载预置文档
            if self._store.count() == 0:
                self._store.add_documents(
                    POLICY_DOCUMENTS,
                    metadatas=[{"source": "policy", "doc_id": i} for i in range(len(POLICY_DOCUMENTS))],
                )
        return self._store

    def search(self, query: str, top_k: int = 3) -> List[Dict[str, Any]]:
        """检索相关政策文档"""
        return self.store.search(query, top_k)

    def answer_question(self, question: str, user_level: str = "", user_dept: str = "") -> Dict[str, Any]:
        """
        Agentic RAG 模式回答问题
        检索 → 推理 → 校验
        """
        # Step 1: 检索相关文档
        results = self.search(question, top_k=3)

        if not results:
            return {
                "answer": "抱歉，未找到相关的财务制度信息。建议您咨询财务部门。",
                "sources": [],
                "confidence": 0.0,
            }

        # Step 2: 推理 - 从检索结果中提取关键信息
        context = "\n".join([r["content"] for r in results])
        sources = [{"content": r["content"][:100] + "...", "score": r["score"],
                     "doc_id": r.get("metadata", {}).get("doc_id", -1)} for r in results]

        # Step 3: 基于规则的回答生成（降级方案，LLM 可用时会替换）
        answer = self._rule_based_answer(question, context, user_level)

        # Step 4: 校验 - 检查回答是否有依据
        confidence = min(max(r["score"] for r in results), 0.95) if results else 0.0

        return {
            "answer": answer,
            "sources": sources,
            "confidence": confidence,
            "context_used": context[:500],
        }

    def _rule_based_answer(self, question: str, context: str, user_level: str) -> str:
        """基于规则的回答生成（LLM 不可用时的降级方案）"""
        q = question.lower()

        # 差旅费相关
        if any(kw in q for kw in ["差旅", "出差", "住宿", "酒店"]):
            limits = {"专员": 400, "高级专员": 500, "主管": 600, "经理": 800,
                      "高级经理": 1000, "总监": 1500, "副总裁": 2500, "总经理": 5000}
            if user_level:
                for k, v in limits.items():
                    if k in user_level:
                        return f"根据差旅费报销制度，您的职级对应的每日差旅费上限为 ¥{v}。出差前需填写出差审批单，出差结束后5个工作日内提交报销申请。"
            return "差旅费报销标准按职级划分：专员400元/天、高级专员500元/天、主管600元/天、经理800元/天、高级经理1000元/天、总监1500元/天。超出标准需部门经理特批。"

        # 餐饮费相关
        if any(kw in q for kw in ["餐饮", "吃饭", "聚餐", "招待", "宴请"]):
            return "餐饮费标准按职级划分（专员150元/餐、高级专员200元/餐、主管250元/餐、经理300元/餐）。商务宴请需事前审批，单次不超过3000元。"

        # 审批流程
        if any(kw in q for kw in ["审批", "流程", "怎么报", "如何"]):
            return "报销审批流程按金额分级：1000元以下两级审批（部门经理→财务），1000-5000元三级（加财务总监），5000元以上四级（加总经理）。驳回后可修改重新提交。"

        # 发票相关
        if any(kw in q for kw in ["发票", "票据", "凭证"]):
            return "报销需提供合规发票，抬头为公司全称。增值税专用发票30日内认证，电子发票需打印。虚假发票将追究责任。"

        # 预算相关
        if any(kw in q for kw in ["预算", "额度", "剩余"]):
            return "部门预算每年12月编制，按季度分解。使用率超80%预警，超100%冻结非必要报销。超预算需提前申请追加。"

        # 默认：返回最相关的检索结果
        return f"根据财务制度相关规定：{context[:200]}...（更多详情请查阅完整制度文件）"


